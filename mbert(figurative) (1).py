def get_fasttext_embedding(text):
    # Ensure text is a string before splitting
    if not isinstance(text, str):
        # Handle non-string types (e.g., NaN, numbers).
        # You can return a zero vector, or an empty list to be averaged into a zero vector.
        # For simplicity, if it's not a string, treat it as an empty string.
        text = ""

    words = text.split()
    word_vectors = [fasttext_model[word] for word in words if word in fasttext_model]

    if not word_vectors:
        # Return a zero vector of the correct dimension if no words found or text was empty
        if hasattr(fasttext_model, 'vector_size'):
            return np.zeros(fasttext_model.vector_size)
        else:
            return np.zeros(300) # Fallback, assume 300 if vector_size not directly available

    vec = np.mean(word_vectors, axis=0)
    return vec

