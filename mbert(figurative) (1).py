from transformers import AutoModel
class MBERTFastTextClassifier(nn.Module):
    def __init__(self, model_name, ft_dim, num_labels):
        super().__init__()
        self.bert = AutoModel.from_pretrained(model_name)
        self.layernorm = nn.LayerNorm(self.bert.config.hidden_size + ft_dim)
        self.dropout = nn.Dropout(0.3)

        # Two-layer classifier with GELU
        self.fc1 = nn.Linear(self.bert.config.hidden_size + ft_dim, 768)
        self.gelu1 = nn.GELU()
        self.fc2 = nn.Linear(768, 256)
        self.gelu2 = nn.GELU()
        self.fc3 = nn.Linear(256, num_labels)

    def forward(self, input_ids, attention_mask, ft_vec):
        bert_out = self.bert(input_ids=input_ids, attention_mask=attention_mask)
        cls_output = bert_out.last_hidden_state[:, 0, :]

        # normalize & scale FastText embeddings
        ft_vec = F.normalize(ft_vec, p=2, dim=1) * 1.0

        combined = torch.cat((cls_output, ft_vec), dim=1)
        combined = self.layernorm(combined)

        x = self.fc1(combined)
        x = self.gelu1(x)
        x = self.dropout(x)
        x = self.fc2(x)
        x = self.gelu2(x)
        x = self.dropout(x)
        logits = self.fc3(x)
        return logits



