class AmharicDataset(Dataset):
    def __init__(self, texts, labels, fasttext_embeddings):
        self.encodings = tokenizer(texts.tolist(), truncation=True, padding=True, max_length=128)
        self.labels = labels.tolist()
        self.fasttext_embeddings = np.stack(fasttext_embeddings.tolist())

    def __getitem__(self, idx):
        item = {key: torch.tensor(val[idx]) for key, val in self.encodings.items()}
        item['labels'] = torch.tensor(self.labels[idx])
        item['fasttext'] = torch.tensor(self.fasttext_embeddings[idx], dtype=torch.float32)
        return item

    def __len__(self):
        return len(self.labels)

