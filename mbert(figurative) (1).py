# Model: extend mBERT to concatenate FastText features
class mBERTWithFastText(torch.nn.Module):
    def __init__(self, num_labels):
        super(mBERTWithFastText, self).__init__()
        self.bert = BertModel.from_pretrained("bert-base-multilingual-cased")
        self.fc = torch.nn.Linear(self.bert.config.hidden_size + 300, 128)
        self.classifier = torch.nn.Linear(128, num_labels)
        self.dropout = torch.nn.Dropout(0.5)

    def forward(self, input_ids, attention_mask, fasttext, labels=None):
        outputs = self.bert(input_ids=input_ids, attention_mask=attention_mask)
        pooled_output = outputs.pooler_output
        combined = torch.cat((pooled_output, fasttext), dim=1)
        x = self.dropout(self.fc(combined))
        logits = self.classifier(x)
        loss = None
        if labels is not None:
            loss_fn = torch.nn.CrossEntropyLoss()
            loss = loss_fn(logits, labels)
        return (loss, logits) if loss is not None else logits

