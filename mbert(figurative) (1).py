def evaluate(model, dataloader):
    model.eval()  # Set model to evaluation mode
    all_labels = []
    all_preds = []
    all_probs = []

    with torch.no_grad():
        for batch in dataloader:
            inputs = batch['input_ids'].to(DEVICE)
            attention_mask = batch['attention_mask'].to(DEVICE)  # Assuming this is part of your input
            ft_vec = batch['fasttext'].to(DEVICE)  # Assuming this is part of your input as well
            labels = batch['labels'].to(DEVICE)

            outputs = model(inputs, attention_mask=attention_mask, ft_vec=ft_vec)  # Call with all required inputs

            probs = torch.softmax(outputs, dim=1)  # Convert logits to probabilities
            preds = torch.argmax(probs, dim=1)  # Get the predicted class

            all_labels.extend(labels.cpu().numpy())
            all_preds.extend(preds.cpu().numpy())
            all_probs.extend(probs.cpu().numpy())

    # Convert lists to numpy arrays
    all_labels = np.array(all_labels)
    all_preds = np.array(all_preds)
    all_probs = np.array(all_probs)

    # Check the shape of all_probs
    if all_probs.ndim == 1:
        all_probs = all_probs.reshape(-1, 1)

    auc = roc_auc_score(all_labels, all_probs, multi_class='ovr')  # Adjust as necessary
    report = classification_report(all_labels, all_preds, digits=4)


    print(report)

    return all_labels, all_preds, all_probs, auc

